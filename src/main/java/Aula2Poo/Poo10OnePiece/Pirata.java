package Aula2Poo.Poo10OnePiece;

public abstract class Pirata {

    // Atributos
    private String nome;
    private char sexo;

    // Get e Set
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

}
